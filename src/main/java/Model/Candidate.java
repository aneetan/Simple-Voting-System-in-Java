package Model;

public class Candidate {
    private int canId;
    private String candidateProfile;
    private String fullNameCandidate;
    private String emailCandidate;
    private int ageCandidate;
    private String addressCandidate;
    private String genderCandidate;
    private String candidate;
    private String experience;
    private int votes;

    public int getCanId() {
        return canId;
    }

    public void setCanId(int canId) {
        this.canId = canId;
    }

    public String getCandidateProfile() {
        return candidateProfile;
    }

    public void setCandidateProfile(String candidateProfile) {
        this.candidateProfile = candidateProfile;
    }

    public String getFullNameCandidate() {
        return fullNameCandidate;
    }

    public void setFullNameCandidate(String fullNameCandidate) {
        this.fullNameCandidate = fullNameCandidate;
    }

    public String getEmailCandidate() {
        return emailCandidate;
    }

    public void setEmailCandidate(String emailCandidate) {
        this.emailCandidate = emailCandidate;
    }

    public int getAgeCandidate() {
        return ageCandidate;
    }

    public void setAgeCandidate(int ageCandidate) {
        this.ageCandidate = ageCandidate;
    }

    public String getAddressCandidate() {
        return addressCandidate;
    }

    public void setAddressCandidate(String addressCandidate) {
        this.addressCandidate = addressCandidate;
    }

    public String getGenderCandidate() {
        return genderCandidate;
    }

    public void setGenderCandidate(String genderCandidate) {
        this.genderCandidate = genderCandidate;
    }

    public String getCandidate() {
        return candidate;
    }

    public void setCandidate(String candidate) {
        this.candidate = candidate;
    }

    public String getExperience() {
        return experience;
    }

    public void setExperience(String experience) {
        this.experience = experience;
    }

    public int getVotes() {
        return votes;
    }

    public void setVotes(int votes) {
        this.votes = votes;
    }
}
